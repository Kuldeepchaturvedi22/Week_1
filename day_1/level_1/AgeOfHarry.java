// Imports the utility classes from the java.util package
import java.util.*; 

// Defines the AgeOfHarry class, which will determine and display Harry's age in 2024
public class AgeOfHarry { 
    // Main method: the entry point of the program
    public static void main(String[] args) { 
        // Calculates Harry's age in 2024 (assuming he was born in 2000)
        int currentAge = 2024 - 2000; 
        // Prints Harry's age to the console
        System.out.println("Harry's age in 2024 is " + currentAge); 
    }
}

